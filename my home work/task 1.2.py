
num1 = input("Enter the first number: ")
oper = raw_input("Select operation +,-,*,/ : ")
num2 = input("Enter the second number: ")

if oper == "+":
    ans=num1+num2
    print (ans)
else:
    pass

if oper == "-":
    ans=num1-num2
    print (ans)
else:
    pass

if oper == "*":
    ans=num1*num2
    print (ans)
else:
    pass

if oper == "/":
    ans=num1/num2
    print (ans)
else:
    pass
